#include "icons.h"
#include <stdint.h>

/**
 * @brief Type definition of a line of a character
 */
typedef uint32_t line_t;

/**
 * @brief Values a bit in the bitmaps can have
 */
typedef enum {
    BACKGROUND = 0,  ///< Background = 0
    FOREGROUND,      ///< Foreground = 1
} pixelType_e;

/**
 * @brief Characters array forward-declaration
 */
extern const line_t verdana_36ptBitmaps[NB_CHARACTERS][VERDANA_NB_ROWS];

/**
 * @brief Uncompress a row of a character from the array to a buffer
 * @note Each character is represented with a bit for each pixel (values are either background or foreground)
 * 
 * @param[out] buffer Buffer to fill with the pixel values
 * @param character Character to uncompress
 */
void uncompressCharacter(pixel_t buffer[], verdanaCharacter_e character) {
    for(uint8_t row = 0; row < (uint8_t)VERDANA_NB_ROWS; row++) {
        for(uint8_t column = 0; column < (uint8_t)VERDANA_NB_COLUMNS; column++) {
            //create a mask showing the nature of the current pixel (foreground or background)
            line_t pixelMask = (line_t)1U << ((line_t)VERDANA_NB_COLUMNS - column + 2U - 1U);

            //get the proper colour to fill
            pixel_t colour = ((verdana_36ptBitmaps[character][row] & pixelMask) == BACKGROUND ? DARK_CHARCOAL_BIGENDIAN
                                                                                              : BRIGHT_GRAY_BIGENDIAN);

            //set the pixel colour in the buffer
            buffer[(row * VERDANA_NB_COLUMNS) + column] = colour;
        }
    }
}

